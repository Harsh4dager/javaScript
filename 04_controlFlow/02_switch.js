
const weekDay = 4

switch (weekDay){
    case 1:
        console.log("mon")
        break
    case 2:
        console.log("tues")
        break
    case 3:
        console.log("wed")
        break
    case 4:
        console.log("thrs")
        break
    case 5:
        console.log("fri")
        break
    case 6:
        console.log("sat")
        break
    default:
        console.log("sun")
}